package com.neusoft.bean.lybean;

public class ImgInfo {
	private int id;
	private int tieziid;
	private String img;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getTieziid() {
		return tieziid;
	}

	public void setTieziid(int tieziid) {
		this.tieziid = tieziid;
	}

	public String getImg() {
		return img;
	}

	public void setImg(String img) {
		this.img = img;
	}

}
